         *  create array with empty L10N data
         *  to override faker's default values
         *
         *  seeding for a non-exisiting locale throws an error 'array to string conversion' because translatable needs to recognise locales and cast them as arrays
         */
        $a_langs = array_keys(config('translatable.languages'));
        $a_iso = array_keys($a_country_list[$a_langs[0]]);

        $a_data_extra =
        [
            'published' => 1,
        ];

        for ($i = 0; $i < count($a_iso); $i++)
        {
            $s_iso = $a_iso[$i];

            $a_data_tmp = [];
            $a_data_tmp['slug'] = $s_iso;

            for ($j = 0; $j < count($a_langs); $j++)
            {
                $s_lang = $a_langs[$j];
                if (isset($a_country_list[$s_lang][$s_iso]))
                {
                    $s_name_translated = $a_country_list[$s_lang][$s_iso];
                    $a_data_tmp[$s_lang] =
                        [
                            'title' => $s_name_translated,
                        ];
                }
            }
            self::_fillData($a_data_tmp, $a_data_extra);
        }

        echo '|' . 'seeded countries only for exisiting locale(s): ' . implode(', ', $a_langs) . '|' . "\n";
    }
